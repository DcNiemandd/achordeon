// Generates apps/app/src/app/supabase.config.ts from env — ONE flow, local + CI.
//
// Precedence (first defined wins):
//   1. process.env         — CI sets SUPABASE_URL / SUPABASE_ANON_KEY as repo
//                            variables (.github/workflows/deploy.yml).
//   2. .env.local (repo root, gitignored) — local dev; copy from
//                            .env.local.example. Same two variables as CI.
//   3. built-in local `supabase start` defaults — so a bare checkout still boots.
//
// The anon key is PUBLIC by design (the browser holds it; RLS is the guard,
// ADR-0003), so it lives in an env file, not a secret store. SUPABASE_URL=''
// builds with no backend (offline-only): the Audience UI reports itself
// unavailable instead of erroring.

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(here, '..'); // apps/app
const repoRoot = resolve(projectRoot, '..', '..');

// Local `supabase start` defaults (deterministic for the current CLI; the anon
// key is derived from the fixed demo JWT secret). If `supabase status` shows a
// different key, put it in .env.local.
const LOCAL_URL = 'http://127.0.0.1:54321';
const LOCAL_ANON_KEY =
  'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZS1kZW1vIiwicm9sZSI6ImFub24iLCJleHAiOjE5ODM4MTI5OTZ9.CRXP1A7WOeoJeXxjNni43kdQwgnWNReilDMblYTn_I0';

/** Minimal KEY=VALUE parser — no dependency. Missing file → {}. */
function readEnvFile(path) {
  let text;
  try {
    text = readFileSync(path, 'utf8');
  } catch {
    return {};
  }
  const out = {};
  for (const raw of text.split('\n')) {
    const line = raw.trim();
    if (line === '' || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq < 0) continue;
    const key = line.slice(0, eq).trim();
    let value = line.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

const fileEnv = readEnvFile(resolve(repoRoot, '.env.local'));

// process.env wins over the file so CI overrides local (`??` keeps an explicit
// '' — "no backend" — from either source).
const pick = (key) => process.env[key] ?? fileEnv[key];

const url = pick('SUPABASE_URL') ?? LOCAL_URL;
const anonKey = pick('SUPABASE_ANON_KEY') ?? LOCAL_ANON_KEY;

const config = url === '' ? null : { url, anonKey };

const out = `// GENERATED by apps/app/tools/gen-supabase.mjs — do not edit by hand.
// Source: process.env, else .env.local, else local \`supabase start\` defaults.
// Regenerate: node apps/app/tools/gen-supabase.mjs
//
// The anon key is public by design — RLS is the guard, not secrecy (ADR-0003).

import type { SupabaseConfig } from '@achordeon/shared/data-access';

export const SUPABASE: SupabaseConfig | null = ${JSON.stringify(config, null, 2)};
`;

writeFileSync(resolve(projectRoot, 'src/app/supabase.config.ts'), out);
