// Generates apps/app/src/app/supabase.config.ts from the environment.
//
// The anon key is PUBLIC by design (the browser holds it; RLS is the guard,
// ADR-0003), so it is not a secret — but it still must not be hand-edited before
// every PR. So the config is generated, never committed:
//
//   - dev  (no env set): the local `supabase start` defaults, identical on every
//                        machine — the app talks to the local stack out of the box.
//   - prod (CI sets env): SUPABASE_URL + SUPABASE_ANON_KEY from repo variables,
//                        injected by .github/workflows/deploy.yml at build time.
//
// Set SUPABASE_URL='' to build with no backend (offline-only): the Audience UI
// then reports itself unavailable instead of erroring.

import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(here, '..');

// Local `supabase start` defaults (fixed demo values, same on every machine).
const LOCAL_URL = 'http://127.0.0.1:54321';
const LOCAL_ANON_KEY =
  'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJyb2xlIjoiYW5vbiIsImlzcyI6InN1cGFiYXNlLWRlbW8iLCJpYXQiOjE2NDE3NjkyMDAsImV4cCI6MTc5OTUzNTYwMH0.dc_X5iR_VP_qT0zsiyj_I_OZ2T9FtRU2BBNWN8Bu4GE';

// `??` not `||`: an explicit SUPABASE_URL='' means "no backend" and must win.
const url = process.env.SUPABASE_URL ?? LOCAL_URL;
const anonKey = process.env.SUPABASE_ANON_KEY ?? LOCAL_ANON_KEY;

const config = url === '' ? null : { url, anonKey };

const out = `// GENERATED by apps/app/tools/gen-supabase.mjs — do not edit by hand.
// Source: env SUPABASE_URL / SUPABASE_ANON_KEY, else local \`supabase start\`
// defaults. Regenerate: node apps/app/tools/gen-supabase.mjs
//
// The anon key is public by design — RLS is the guard, not secrecy (ADR-0003).

import type { SupabaseConfig } from '@achordeon/shared/data-access';

export const SUPABASE: SupabaseConfig | null = ${JSON.stringify(config, null, 2)};
`;

writeFileSync(resolve(projectRoot, 'src/app/supabase.config.ts'), out);
